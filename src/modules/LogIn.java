    public void dbexecute() 
	{
		String user= txtUser.getText();
		@SuppressWarnings("deprecation")
		String pass= txtPass.getText().toString();
		
		if(user.equals("") || pass.equals("")) 
		{
			lblMsg.setText("Fields Cannot Be Empty");
			txtUser.setText("");
			txtPass.setText("");	
		}
		else
		{
		try
		{
			Class.forName("com.mysql.jdbc.Driver");
			Connection con= DriverManager.getConnection("jdbc:mysql://localhost:3306/bloodbank","root","");
			Statement st=con.createStatement();
		
			
			String sql= "select * from Admins where Username= '"+txtUser.getText()+"' and Password='"+txtPass.getText().toString()+"'";
			
			ResultSet rs=st.executeQuery(sql);
			if(rs.next())
			{
				user=txtUser.getText();
				 Dashboard db= new Dashboard();
                                 db.setVisible(true);
                                 db.lblAdmin.setText(user);
                                 this.dispose();
                                
			}
			else
			{
				lblMsg.setText("Invalid Credentials");
				txtUser.setText("");
				txtPass.setText("");				
			}
			con.close();
		}
		catch(Exception e)
		{
			lblMsg.setText("Database Error");
		}
		}

		
	}
    
